def render_corep_html(corep_output: Dict[str, Any]) -> str:
    """
    Render COREP output as HTML table.
    
    Args:
        corep_output: Dictionary containing COREP fields and metadata
    
    Returns:
        HTML string with formatted COREP template
    """
    template = Template(C01_HTML_TEMPLATE)
    
    # Extract currency from first field or default to GBP
    currency = "GBP"
    if corep_output.get("fields") and len(corep_output["fields"]) > 0:
        currency = corep_output["fields"][0].get("currency", "GBP")
    
    html = template.render(
        fields=corep_output.get("fields", []),
        validation_warnings=corep_output.get("validation_warnings", []),
        currency=currency
    )
    
    return html
